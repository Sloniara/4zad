def uppercase_string(input_string):
    """
        Принимает на вход строку и возвращает ее со всеми заглавными буквами.

        :param input_string: входная строка
        :return: строка со всеми заглавными буквами
        """
    return input_string.upper()


def capitalize_words(input_string):
    """
    Принимает на вход строку и возвращает ее с заглавными первыми буквами каждого слова.

    :param input_string: входная строка
    :return: строка с заглавными первыми буквами каждого слова
    """
    return input_string.title()